export const REFERENCE_MASK_BOUNDS = {
  minX: -1.25,
  maxX: 1.25,
  minY: -1.08,
  maxY: 1.48,
} as const

const clamp = (value: number, min: number, max: number): number =>
  Math.min(max, Math.max(min, value))

const sampleCell = (column: number, row: number): number => {
  const safeRow = rows[clamp(row, 0, rows.length - 1)]
  const value = safeRow?.[clamp(column, 0, (safeRow?.length ?? 1) - 1)]

  return value === undefined ? 0 : Number(value) / 9
}

export const sampleReferenceDensity = (x: number, y: number): number => {
  const u = clamp(
    (x - REFERENCE_MASK_BOUNDS.minX) /
      (REFERENCE_MASK_BOUNDS.maxX - REFERENCE_MASK_BOUNDS.minX),
    0,
    1,
  )
  const v = clamp(
    (REFERENCE_MASK_BOUNDS.maxY - y) /
      (REFERENCE_MASK_BOUNDS.maxY - REFERENCE_MASK_BOUNDS.minY),
    0,
    1,
  )
  const width = rows[0]?.length ?? 1
  const height = rows.length
  const px = u * (width - 1)
  const py = v * (height - 1)
  const left = Math.floor(px)
  const top = Math.floor(py)
  const right = Math.min(width - 1, left + 1)
  const bottom = Math.min(height - 1, top + 1)
  const tx = px - left
  const ty = py - top
  const topValue = sampleCell(left, top) * (1 - tx) + sampleCell(right, top) * tx
  const bottomValue = sampleCell(left, bottom) * (1 - tx) + sampleCell(right, bottom) * tx

  return topValue * (1 - ty) + bottomValue * ty
}
